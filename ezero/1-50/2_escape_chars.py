# \b => back space 
# \newline => Escape New Line + \
# \' => Escape Single Quotes
# \" => Escape Double Quotes
# \r => Carriage Return
# \xhh => Character Hex Value

# back space
print("hello\bpython") # hellpython

# \newline
print("hello \
guys \
how \
are you") # hello guys how are you

# single & double quotes 
print("his name is \"ahmed\" ") # his name is "ahmed"
print('his name is \'ahmed\' ') # his name is 'ahmed'

# Carriage Return
print("keddour\r1234") # 1234our  
